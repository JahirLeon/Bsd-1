CREATE DATABASE Banco_Anglo
GO

USE [Banco_Anglo]
GO

SET DATEFORMAT dmy
SET LANGUAGE spanish


--Tabla Banco
CREATE TABLE Banco (
	CODIGO_BANCO int not null,
	NOMBRE varchar(45) not null,
	CONSTRAINT PK_Banco PRIMARY KEY (CODIGO_BANCO ASC)
)
GO


--Tabla Sucursales
CREATE TABLE Sucursales (
	CODIGO_SUCURSAL INT NOT NULL,
	NOMBRE VARCHAR(45) NOT NULL,
	CODIGO_BANCO INT NOT NULL,
	CONSTRAINT PK_Sucursales PRIMARY KEY (CODIGO_SUCURSAL),
	CONSTRAINT FK_Banco_Sucursales FOREIGN KEY (CODIGO_BANCO) REFERENCES Banco(CODIGO_BANCO)
)
GO


--Tabla Empleados
CREATE TABLE Empleados (
	ID_EMPLEADO INT NOT NULL,
	NOMBRE VARCHAR(45) NOT NULL,
	FECHA_ASIGNACION_SUC DATE NOT NULL,
	TELEFONO VARCHAR(10) NOT NULL,
	FECHA_INICIO_LABORO DATE NOT NULL,
	CODIGO_SUCURSAL INT NOT NULL
	CONSTRAINT PK_Empleados PRIMARY KEY (ID_EMPLEADO),
	CONSTRAINT UN_Empleado_Telefono UNIQUE (TELEFONO),
	CONSTRAINT FK_Sucursales_Empleados FOREIGN KEY (CODIGO_SUCURSAL) REFERENCES Sucursales(CODIGO_SUCURSAL)
)
GO


--Tabla de Tipo Préstamo
CREATE TABLE Tipo_Prestamo (
	ID_TIPO_PRESTAMO INT NOT NULL,
	NOMBRE VARCHAR(45) NOT NULL,
	PLAZO_MIN VARCHAR(50) NOT NULL,
	TASA_INTERES VARCHAR(45) NOT NULL,
	PLAZO_MAX VARCHAR(50) NOT NULL,
	MONEDA VARCHAR(20) NOT NULL,
	MONTO_MIN NUMERIC (14, 2) NOT NULL,
	MONTO_MAX NUMERIC (14, 2) NOT NULL,
	CONSTRAINT PK_Tipo_Prestamo PRIMARY KEY (ID_TIPO_PRESTAMO ASC)
)
GO


--Tabla Actividad Económica
CREATE TABLE Actividad_Economica (
	CODIGO_ACTIVIDAD INT NOT NULL,
	NOMBRE_ACTIVIDAD VARCHAR(45) NOT NULL,
	CONSTRAINT PK_Actidad_Economica PRIMARY KEY (CODIGO_ACTIVIDAD ASC)
)
GO


--Tabla Provincias
CREATE TABLE Provincias (
	CODIGO_PROVINCIA INT NOT NULL,
	NOMBRE_PROVINCIA VARCHAR(15),
	CONSTRAINT PK_Provincias PRIMARY KEY (CODIGO_PROVINCIA ASC)
)
GO


--Tabla Clientes
CREATE TABLE Clientes (
	ID_CLIENTE INT NOT NULL,
	NOMBRE VARCHAR(45) NOT NULL,
	CORREO VARCHAR(30) NOT NULL,
	DIRECCION VARCHAR(80) NOT NULL,
	INGRESO_MENSUAL NUMERIC(10, 2) NOT NULL,
	CODIGO_ACTIVIDAD INT NOT NULL,
	CODIGO_PROVINCIA INT NOT NULL,
	CONSTRAINT PK_Clientes PRIMARY KEY (ID_CLIENTE ASC),
	CONSTRAINT FK_Actividad_Economica_Clientes FOREIGN KEY (CODIGO_ACTIVIDAD) REFERENCES Actividad_Economica(CODIGO_ACTIVIDAD),
	CONSTRAINT FK_Provincias_Clientes FOREIGN KEY (CODIGO_PROVINCIA) REFERENCES Provincias(CODIGO_PROVINCIA),
	CONSTRAINT CH_INGRESO_MENSUAL_POSITIVO CHECK (INGRESO_MENSUAL > 0)
)
GO


--Tabla Teléfonos
CREATE TABLE Telefonos (
	CODIGO_TELEFONO INT NOT NULL,
	NUMERO_TELEFONO VARCHAR(10) NOT NULL,
	ID_CLIENTE INT NOT NULL,
	CONSTRAINT PK_Telefonos PRIMARY KEY (CODIGO_TELEFONO ASC),
	CONSTRAINT FK_Clientes_Telefonos FOREIGN KEY (ID_CLIENTE) REFERENCES Clientes(ID_CLIENTE)
)
GO


--Tabla Préstamos
CREATE TABLE Prestamos (
	NUMERO_PRESTAMO INT NOT NULL,
	FECHA_PRESTAMO_OTORGADO DATE NOT NULL,
	MONTO_PRESTAMO NUMERIC(10, 2) NOT NULL,
	TASA VARCHAR(50) NOT NULL,
	PLAZO VARCHAR(50) NOT NULL,
	CUOTA_MENSUAL NUMERIC(10, 2) NOT NULL,
	ID_EMPLEADO INT NOT NULL,
	CODIGO_SUCURSAL INT NOT NULL,
	ID_CLIENTE INT NOT NULL,
	ID_TIPO_PRESTAMO INT NOT NULL,
	CONSTRAINT PK_Prestamos PRIMARY KEY (NUMERO_PRESTAMO),
	CONSTRAINT FK_Empleados_Prestamos FOREIGN KEY (ID_EMPLEADO) REFERENCES Empleados(ID_EMPLEADO),
	CONSTRAINT FK_Sucursales_Prestamos FOREIGN KEY (CODIGO_SUCURSAL) REFERENCES Sucursales(CODIGO_SUCURSAL),
	CONSTRAINT FK_Clientes_Prestamos FOREIGN KEY (ID_CLIENTE) REFERENCES Clientes(ID_CLIENTE),
	CONSTRAINT FK_ID_TIPO_PRESTAMO FOREIGN KEY (ID_TIPO_PRESTAMO) REFERENCES Tipo_Prestamo(ID_TIPO_PRESTAMO)
)
GO



--Tabla Pagos
CREATE TABLE Pagos (
	CODIGO_PAGO INT NOT NULL, 
	MONTO_DEL_PAGO NUMERIC(10, 2) NOT NULL,
	FECHA_DEL_PAGO DATE NOT NULL,
	ID_EMPLEADO INT NOT NULL,
	ID_CLIENTE INT NOT NULL,
	NUMERO_PRESTAMO INT NOT NULL,
	CONSTRAINT PK_Pagos PRIMARY KEY (CODIGO_PAGO ASC),
	CONSTRAINT FK_Empleados_Pagos FOREIGN KEY (ID_EMPLEADO) REFERENCES Empleados(ID_EMPLEADO),
	CONSTRAINT FK_Clientes_Pagos FOREIGN KEY (ID_CLIENTE) REFERENCES Clientes(ID_CLIENTE),
	CONSTRAINT FK_Prestamos_Pagos FOREIGN KEY (NUMERO_PRESTAMO) REFERENCES Prestamos(NUMERO_PRESTAMO)
)
GO



