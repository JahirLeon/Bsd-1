--1.Listar todos los préstamos de un cliente en particular. en un Rango de Fechas. Mostrando código y nombre del cliente, el código, la fecha y el monto del préstamo.
SELECT P.[NUMERO_PRESTAMO], C.[NOMBRE], C.[ID_CLIENTE], P.[FECHA_PRESTAMO_OTORGADO], P.[MONTO_PRESTAMO]
	FROM [dbo].[Prestamos] P
	INNER JOIN [dbo].[Clientes] C ON P.ID_CLIENTE = C.ID_CLIENTE
	WHERE ([FECHA_PRESTAMO_OTORGADO] >= '2000-09-18' AND [FECHA_PRESTAMO_OTORGADO]<= '2007-04-20') AND C.[ID_CLIENTE] = 8
	ORDER BY [FECHA_PRESTAMO_OTORGADO]

--2.	Listar todos los empleados de una sucursal en particular: Mostrando código y nombre de la sucursal, el código, nombre del empleado.
SELECT S.[CODIGO_SUCURSAL], S.[NOMBRE] as NOMBRE_SUCURSAL, E.[ID_EMPLEADO], E.[NOMBRE] as NOMBRE_EMPLEADO
	FROM [dbo].[Sucursales] S, [dbo].[Empleados] E
	WHERE S.[CODIGO_SUCURSAL]=2



